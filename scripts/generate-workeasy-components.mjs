import { access, mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import { dirname, extname, resolve } from "node:path";

const repoRoot = resolve(import.meta.dirname, "..");

// WorkEasy 源仓库路径：优先读取环境变量，缺省时回退到 ai-motion 同级目录的 jdc-WorkEasy。
const workEasyRoot = process.env.WORKEASY_ROOT
  ? resolve(process.env.WORKEASY_ROOT)
  : resolve(repoRoot, "..", "jdc-WorkEasy");

try {
  await access(workEasyRoot);
} catch {
  console.error(
    `[generate-workeasy-components] 找不到 WorkEasy 源仓库：${workEasyRoot}\n` +
      `请将 jdc-WorkEasy 克隆到 ai-motion 的同级目录，或设置环境变量 WORKEASY_ROOT 指向其路径。\n` +
      `示例：WORKEASY_ROOT=/path/to/jdc-WorkEasy pnpm gen:workeasy`
  );
  process.exit(1);
}

const categories = ["buttons", "cards", "checkboxes"];

async function readFallbackFile(category, id, extension) {
  const componentDir = resolve(workEasyRoot, "content", category, id);
  try {
    const files = await readdir(componentDir);
    const file = files.find((item) => extname(item) === extension);
    if (!file) return undefined;

    const content = await readFile(resolve(componentDir, file), "utf8");
    return content.trim() ? content : undefined;
  } catch {
    return undefined;
  }
}

async function normalizeRecord(category, record) {
  const htmlContent = record.htmlContent ?? (await readFallbackFile(category, record.id, ".html"));
  const cssContent = record.cssContent ?? (await readFallbackFile(category, record.id, ".css"));

  return {
    category,
    record: {
      id: record.id,
      title: record.title,
      author: record.author,
      type: record.type,
      framework: record.framework,
      tags: record.tags,
      description: record.description,
      version: record.version,
      htmlContent,
      cssContent,
      jsContent: record.jsContent,
      tsxContent: record.tsxContent,
      jsxContent: record.jsxContent,
      vueContent: record.vueContent
    }
  };
}

async function readCategory(category) {
  const path = resolve(workEasyRoot, "public", "api", "components", `${category}.json`);
  const records = JSON.parse(await readFile(path, "utf8"));
  return Promise.all(records.map((record) => normalizeRecord(category, record)));
}

const inputs = (await Promise.all(categories.map((category) => readCategory(category)))).flat();

const output = `/* eslint-disable */
// @generated by scripts/generate-workeasy-components.mjs
// 请勿手动修改，运行 \`pnpm gen:workeasy\` 重新生成。

import type { WorkEasyCategory, WorkEasyComponentRecord } from "@motion-tool/core";

export const generatedWorkEasyRecords: Array<{ category: WorkEasyCategory; record: WorkEasyComponentRecord }> = ${JSON.stringify(
  inputs,
  null,
  2
)};
`;

const target = resolve(repoRoot, "apps", "web", "src", "data", "workeasyComponents.generated.ts");
await mkdir(dirname(target), { recursive: true });
await writeFile(target, output);
console.log(`Generated ${inputs.length} WorkEasy records at ${target}`);
